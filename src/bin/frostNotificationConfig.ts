import 'dotenv/config';
import { existsSync, readFileSync, writeFileSync } from 'fs';
import { resolve } from 'path';
import { Logger, LogPrinter } from 'esm-iso-logger';
import { getAbsolutePath } from '../utils.js';
import { getSodium } from '../dhHmacJwt.js';
import { rootDir } from '../path.js';

const logger = new Logger('FrostNotificationConfig');
new LogPrinter('FrostNotificationConfig');

const possibleConfigPath = process.env.FROST_CONFIG_PATH;

const issues: string[] = [];
if (!possibleConfigPath) issues.push('Missing required env: FROST_CONFIG_PATH — path to your FROST config file (e.g. ~/.config/frost/config)');

if (possibleConfigPath) {
    const absolutePath = getAbsolutePath(possibleConfigPath);
    if (!existsSync(absolutePath)) {
        issues.push(`FROST config file does not exist: ${absolutePath}. Run npm run frost-init first.`);
    }
}

if (issues.length || !possibleConfigPath) {
    logger.warn('Could not continue due to the following issues:');
    issues.forEach((issue) => logger.error(`  - ${issue}`));
    logger.fatal('Encountered a fatal error and cannot continue.');
    process.exit(1);
}

const configFilePath = getAbsolutePath(possibleConfigPath);
const content = readFileSync(configFilePath, 'utf8');

// Extract own communication public key
const ownPubkeyMatch = content.match(/\[communication_key\][\s\S]*?pubkey\s*=\s*"([^"]+)"/);
if (!ownPubkeyMatch) issues.push('No communication key found in FROST config. Run frost-init first.');

const pubkeys: string[] = [];
if (ownPubkeyMatch) pubkeys.push(ownPubkeyMatch[1]);

// Extract all contact public keys
const contactPubkeyMatches = content.matchAll(/\[contact\.[^\]]+\][\s\S]*?pubkey\s*=\s*"([^"]+)"/g);
for (const match of contactPubkeyMatches) {
    pubkeys.push(match[1]);
}

if (pubkeys.length < 2) issues.push('Not enough contacts found in FROST config. Import contacts with npm run frost-import first.');

if (issues.length) {
    logger.warn('Could not continue due to the following issues:');
    issues.forEach((issue) => logger.error(`  - ${issue}`));
    logger.fatal('Encountered a fatal error and cannot continue.');
    process.exit(1);
}

// Generate server X25519 keypair
const sodium = await getSodium();

const serverPrivBytes = sodium.randombytes_buf(32);
const serverPubBytes = sodium.crypto_scalarmult_base(serverPrivBytes);
const serverPrivkeyHex = Buffer.from(serverPrivBytes).toString('hex');
const serverPubkeyHex = Buffer.from(serverPubBytes).toString('hex');

// Write complete env file
const outputPath = resolve(rootDir, '..', '.env.notification-server');
const envContent = [
    '# Notification service environment',
    '# Generated by npm run frost-notification-config',
    '# Fill in TELEGRAM_BOT_TOKEN and TELEGRAM_CHAT_ID then copy to docker/',
    '',
    `NOTIFICATION_NAMESPACE=nori-multisig`,
    `NOTIFICATION_ALLOWED_KEYS=${pubkeys.join(',')}`,
    `NOTIFICATION_SERVER_PRIVKEY=${serverPrivkeyHex}`,
    `TELEGRAM_BOT_TOKEN=<set this>`,
    `TELEGRAM_CHAT_ID=<set this>`,
    '',
].join('\n');

writeFileSync(outputPath, envContent, { mode: 0o600 });

logger.log(`Generated notification service config: ${outputPath}`);
logger.log('');
logger.log('Copy the values into docker/.env.notification-server on the server.');
logger.log('Fill in TELEGRAM_BOT_TOKEN and TELEGRAM_CHAT_ID.');
logger.log('See docker/README.md for deployment instructions.');
logger.log('');
logger.log(`Server public key: ${serverPubkeyHex}`);
logger.log('After deploying, verify with: curl https://notify.yourdomain.com/pubkey');
